export function getBusinessesByCategory(categorySlug: string): Business[] {
  return businesses.filter((b) => b.categorySlug === categorySlug);
}

export function getBusinessBySlug(slug: string): Business | undefined {
  return businesses.find((b) => b.slug === slug);
}

export function getBusinessById(id: string): Business | undefined {
  return businesses.find((b) => b.id === id);
}

export function searchBusinesses(query: string, filters?: { category?: string; location?: string }): Business[] {
  const q = query.toLowerCase();
  return businesses.filter((b) => {
    const matchesQuery =
      !q ||
      b.name.toLowerCase().includes(q) ||
      b.description.toLowerCase().includes(q) ||
      b.services.some((s) => s.toLowerCase().includes(q)) ||
      b.tags.some((t) => t.toLowerCase().includes(q));

    const matchesCategory = !filters?.category || b.categorySlug === filters.category;
    const location = filters?.location?.toLowerCase() ?? "";
    const matchesLocation =
      !location ||
      b.city.toLowerCase().includes(location) ||
      b.servingAreas.some((a) => a.toLowerCase().includes(location));

    return matchesQuery && matchesCategory && matchesLocation;
  });
}

export function getFeaturedBusinesses(): Business[] {
  return businesses.filter((b) => b.featured);
}
